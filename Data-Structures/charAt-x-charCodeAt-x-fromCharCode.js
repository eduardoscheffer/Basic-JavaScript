// O método String.fromCharCode() retorna uma string criada ao usar uma sequência específica de valores Unicode.
console.log(String.fromCharCode(65, 66, 67, 68, 69)); // ABCDE



let name = "Eduardo Mengue Scheffer"
// O método charAt() retorna o caractere especificado a partir de uma string.
console.log(name.charAt(1)); // d

// O método charCodeAt() retorna um número inteiro entre 0 e 65535 que representa a unidade de código UTF-16 no índice fornecido.
console.log(name.charCodeAt(0)); // 69