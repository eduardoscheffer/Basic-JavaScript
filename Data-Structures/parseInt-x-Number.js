// conversão de número
console.log(parseInt('12')); // 12
console.log(Number('12')); // 12

// conversão de valor não numerico:
console.log(parseInt('teste')); // NaN
console.log(Number('teste')); // NaN

// conversão de float:
console.log(parseInt('12.7')); // 12
console.log(Number('12.7')); // 12.7
console.log(parseFloat('12.7')); // 12.7

// conversão de expoentes:
console.log(parseInt('1e2')); // 1
console.log(Number('1e2')); // 100

